# In-memory TTL cache for hot, slowly-changing reads (stats, dictionaries, collection counts).
# Aggregates over the full plates table are expensive on a remote DB, so we memoise them.
import time as _time

_CACHE: Dict[str, Any] = {}


def invalidate_cache() -> None:
    """Drop all cached reads (call after a scan/commit changes the data)."""
    _CACHE.clear()


async def warm_cache() -> None:
    """Pre-compute the hot cached reads so the first user doesn't pay the cold cost."""
    try:
        await get_stats()
        await distinct_regions()
        await distinct_vehicle_types()
        await collection_counts()
    except Exception:  # noqa: BLE001
        pass


async def _memo(key: str, ttl: float, fn):
    """Return a cached value for ``key`` (recompute via ``fn`` when stale)."""
    now = _time.monotonic()
    e = _CACHE.get(key)
    if e and now - e[0] < ttl:
        return e[1]
    val = await fn()
    _CACHE[key] = (now, val)
    return val


def _now() -> str:
    """UTC ISO timestamp string (matches db_sqlite so migrated rows compare correctly)."""
    return dt.datetime.now(dt.timezone.utc).isoformat()


def _cutoff(days: int) -> str:
    """ISO timestamp ``days`` in the past — used for time-window filters on TEXT timestamps."""
    return (dt.datetime.now(dt.timezone.utc) - dt.timedelta(days=days)).isoformat()


def _q(sql: str) -> str:
    """Convert SQLite-style ``?`` placeholders to Postgres ``$1, $2, …`` (no literal ? in our SQL)."""
    out: List[str] = []
    i = 0
    for ch in sql:
        if ch == "?":
            i += 1
            out.append(f"${i}")
        else:
            out.append(ch)
    return "".join(out)


def _rowcount(status: str) -> int:
    """Parse asyncpg command status like 'DELETE 5' / 'UPDATE 3' → affected row count."""
    try:
        return int(status.split()[-1])
    except (ValueError, IndexError):
        return 0


async def _get_pool() -> asyncpg.Pool:
    """Lazily create the connection pool (SSL required by Supabase)."""
    global _pool
    if _pool is None:
        ctx = ssl.create_default_context()
        ctx.check_hostname = False
        ctx.verify_mode = ssl.CERT_NONE  # encrypt in transit; Supabase pooler cert not verified
        _pool = await asyncpg.create_pool(
            host=config.PG_HOST, port=config.PG_PORT, database=config.PG_DB,
            user=config.PG_USER, password=config.PG_PASSWORD, ssl=ctx,
            min_size=1, max_size=8, command_timeout=60, statement_cache_size=0,
        )
    return _pool


async def _fetch(sql: str, *args: Any) -> List[asyncpg.Record]:
    pool = await _get_pool()
    async with pool.acquire() as c:
        return await c.fetch(_q(sql), *args)


async def _fetchrow(sql: str, *args: Any) -> Optional[asyncpg.Record]:
    pool = await _get_pool()
    async with pool.acquire() as c:
        return await c.fetchrow(_q(sql), *args)


async def _fetchval(sql: str, *args: Any) -> Any:
    pool = await _get_pool()
    async with pool.acquire() as c:
        return await c.fetchval(_q(sql), *args)


async def _exec(sql: str, *args: Any) -> str:
    pool = await _get_pool()
    async with pool.acquire() as c:
        return await c.execute(_q(sql), *args)


