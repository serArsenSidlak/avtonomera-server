@asynccontextmanager
async def acquire():
    """Yield a transactional connection (commits on clean exit) for multi-step scan writes."""
    pool = await _get_pool()
    async with pool.acquire() as conn:
        tr = conn.transaction()
        await tr.start()
        try:
            yield conn
            await tr.commit()
        except Exception:
            await tr.rollback()
            raise


