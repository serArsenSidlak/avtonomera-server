async def collection_counts() -> List[Dict[str, Any]]:
    """Curated collections with counts (cached — heavy aggregates over the full table)."""
    async def fn():
        out = []
        for k, v in COLLECTIONS.items():
            out.append({"key": k, "label": v, "count": await count_filtered(collection=k)})
        return out
    return await _memo("colcounts", 900, fn)


async def distinct_prices(
    region: Optional[str] = None, tsc: Optional[str] = None,
    vehicle_type: Optional[str] = None, limit: int = 60,
) -> List[float]:
    """Distinct available prices for the given filters, ascending."""
    where = ["price IS NOT NULL", "is_available = 1"]
    params: List[Any] = []
    if region:
        where.append("region = ?")
        params.append(region)
    if tsc:
        where.append("tsc = ?")
        params.append(tsc)
    if vehicle_type:
        where.append("vehicle_type = ?")
        params.append(vehicle_type)
    params.append(limit)
    sql = "SELECT DISTINCT price FROM plates WHERE " + " AND ".join(where) + " ORDER BY price LIMIT ?"
    return [float(r[0]) for r in await _fetch(sql, *params)]


