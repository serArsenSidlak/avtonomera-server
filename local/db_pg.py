async def create_bots(n: int, fav_range=(20, 80), hunt_range=(15, 60)) -> int:
    """Create ``n`` demo-bot users, each with random beautiful favorites + monitorings."""
    now = _now()
    pool = await _get_pool()
    async with pool.acquire() as conn:
        bpool = await _beautiful_pool(conn)
        if not bpool:
            return 0
        digit_pool = list({p["digits"] for p in bpool if p["digits"]})
        min_id = await conn.fetchval("SELECT MIN(chat_id) FROM users WHERE is_bot=1")
        next_id = (min_id - 1) if min_id is not None else -1_000_000_001
        created = 0
        for _ in range(n):
            bid = next_id
            next_id -= 1
            await conn.execute(
                "INSERT INTO users (chat_id, username, created_at, is_bot, shared) "
                "VALUES ($1,$2,$3,1,1) ON CONFLICT DO NOTHING",
                bid, f"bot_{abs(bid) % 1000000}", now,
            )
            favs = random.sample(bpool, min(random.randint(*fav_range), len(bpool)))
            await conn.executemany(
                "INSERT INTO favorites (chat_id, plate_number, digits, region, created_at) "
                "VALUES ($1,$2,$3,$4,$5) ON CONFLICT DO NOTHING",
                [(bid, f["plate_number"], f["digits"], f["region"], now) for f in favs],
            )
            hcombos = random.sample(digit_pool, min(random.randint(*hunt_range), len(digit_pool)))
            await conn.executemany(
                "INSERT INTO hunts (chat_id, name, match_type, pattern, digits_exact, created_at) "
                "VALUES ($1,$2,'digits',$3,$4,$5)",
                [(bid, d, d, d, now) for d in hcombos],
            )
            created += 1
        return created


